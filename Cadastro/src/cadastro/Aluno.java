package cadastro;

public class Aluno {
    String nome, cpf;
    
    
    public Aluno(){
        
    }
    
    public Aluno(String nome, String cpf){
        setNome(nome);
        setCpf(cpf);
    }
    
    public void setNome(String nome){
        this.nome=nome;
    }
    
    public void setCpf(String cpf){
        this.cpf=cpf;
    }
    
    
    
    



    
}
